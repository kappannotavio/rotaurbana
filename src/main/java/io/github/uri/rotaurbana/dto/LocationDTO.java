package io.github.uri.rotaurbana.dto;

public class LocationDTO {

    private double lat;
    private double lon;


    //methods


    //getters and setters
    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }
}
